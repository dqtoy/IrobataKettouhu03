namespace CCGKit
{
    /// <summary>
    /// This class represents a runtime instance of a keyword.
    /// このクラスは、キーワードの実行時インスタンスを表します。
    /// </summary>
    public class RuntimeKeyword
    {
        /// <summary>
        /// The identifier of this keyword.
        /// このキーワードの識別子。
        /// </summary>
        public int keywordId;

        /// <summary>
        /// The value of this keyword.
        /// このキーワードの値。
        /// </summary>
        public int valueId;
    }
}
